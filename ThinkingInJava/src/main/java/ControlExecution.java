public class ControlExecution {

    public static void main(String[] args) {
        switch (5)
        {
            case 1: break;
            case 3:
                System.out.println(6); break;
            default:
                System.out.println("deful");
            case 6:
                System.out.println(9); break;

        }
    }
}
